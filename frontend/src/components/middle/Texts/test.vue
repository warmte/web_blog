        <h2>Technical interviews</h2>
        <p>Очень полезная книжка: Cracking the Coding Interview.</p>
        <p>Всего два интервью, в исключительном случае могут попросить пройти третье. Оба интервью проходят в один день,
            каждое длится 45 минут, перерыв между ними 15 минут. Каждое из интервью проводит Google-фуллтаймер, разные
            интервью проводят разные люди. Созвон происходит с помощью <a
                    href="http://hangouts.google.com/">hangouts</a>, весь код пишется в специально созданный
            для этого <a href="https://docs.google.com/document/u/0/?tgif=d">google doc</a>. Золотое правило: после
            первого интервью, каким бы оно ни было — успешным или
            провальным — забудьте про него и начинайте второе с чистой головой.</p>
        <p>По итогу ваш интервьюер досконально описывает ваше поведение во время интервью и ставит какие-то оценки, так
            что очень важно, чтобы в каждый момент времени он понимал, чем вы сейчас заняты. Думаете над задачей —
            делайте это вслух, не пытайтесь сразу придумать идеальное решение, начните с более простых или
            асимптотически не оптимальных вариантов, потом всегда можно попытаться додумать. </p>
        <p>Интервью проводится на английском, что может стать большим испытанием, но на самом деле сильно высокий
            уровень английского не нужен, главное, чтобы вы смогли понять, чего от вас хотят, и донести, что хотите
            сделать. Не стесняйтесь переспрашивать или просить написать какие-то ключевые моменты в пошаренный гугл док,
            в котором вы будете кодить и который будет доступен вам и вашему интервьюеру. <br>
            Код пишется в гугл доке, что может вызвать дополнительные неудобства, открыть любимую среду разработки с
            автодополнением и шаблонами не выйдет. Поучитесь писать код в таких условиях.</p>
        <p>
            Хорошей практикой считается пройти одно-два mock-interview (пробных интервью) с более опытными друзьями или
            на специальных сервисах, например на <a href="https://www.pramp.com/">pramp.com</a>.<br>
            Задачи на интервью отличаются от обычных олимпиадных тем, что должны решаться и кодиться за довольно
            ограниченное время и редко содержат какие-то сложные структуры данных, так что полезно порешать подобные
            задачи на сервисах вроде <a href="https://leetcode.com/">leetcode.com</a>. Часто бывает, что вы решаете
            задачу на интервью, остается время, и
            интервьюер решает дать вам усложненную модификацию.
        </p>
        <p>На STEP обычно очень простые задачи на отборе (в основном на несложные темы а-ля списки, массивы, бинарные
            деревья поиска (несбалансированные), стеки и очереди, динамика, бинарный поиск, etc.), так что самое важное
            — не волноваться и внимательно слушать то, что говорит вам интервьюер. </p>
