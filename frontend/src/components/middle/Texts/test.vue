        <h2>Host-matching</h2>
        <p>После того, как вы получите письмо об успешном прохождении технических интервью и поболтаете по hangouts со
            своим HR’ом, начнётся пора хост-матчингов. Время от времени рекрутер будет назначать созвон с какой-нибудь
            командой. </p>
        <p>Во время этого созвона вы будете общаться с потенциальными хостами, они расскажут про себя и про проект,
            которым вам предлагается заниматься в течение стажировки, и поспрашивают про вас, ваш опыт в разработке и в
            командной работе в целом. Полезно иметь небольшой заготовленный рассказ о себе и уметь быстро и живо его
            проговаривать.</p>
        <p>По результатам хост-матчинга вы пишете рекрутеру, хотите ли вы работать с этой командой или нет, и команда
            пишет, хочет ли она работать с вами. Если вы оба согласны — матчинг произошёл, добро пожаловать в
            команду! </p>
        <p>Во время разговора проявляйте интерес к проекту, задавайте вопросы и просто будьте дружелюбным, тогда проблем
            с матчингом на понравившийся проект не возникнет :) </p>
